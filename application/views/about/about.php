        <h2>About TweetFinder</h2>
        <br />
        <p>TweetFinder is a simple tool to help you gather Twitter accounts.</p>
        
        <p>For example, it might be useful if you have a list of blogs that you wish to follow on Twitter, paste the links in, wait for results and then click each one you want to follow.</p>
        
        <p>It's not designed to return every Twitter account it finds as generally this isn't helpful and some pages will display tons of user accounts. Instead it visits each URL and looks for the
            Twitter username that's most likely to be related to the site. This means sometimes, it won't return an account at all if it's not sure.</p>
        
        <p>It works pretty well but there are a few ways it can be improved. The code is on <a href="https://github.com/jamesblackwell/tweetfinder">GitHub</a>, I'm happy to accept 
            pull requests. Fork away.</p>
            
